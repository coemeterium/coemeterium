The Cebu Metropolitan Cathedral, also as The Metropolitan Cathedral of the Holy Angels and of St. Vitales (consecrated to the Archangels and all angels and dedicated to the saint), or The Metropolitan Cathedral of St. Vitales is the ecclesiastical seat of the Metropolitan Archdiocese of Cebu in Cebu, Philippines.[1] Cebu was established as a diocese on August 14, 1595. It was elevated as a metropolitan archdiocese on April 28, 1934 with the dioceses of Dumaguete, Maasin, Tagbilaran, and Talibon as suffragans. Before being raised as a primatial church in Cebu, the temple was one of the first churches in the Philippines (besides the Basilica del Santo Niño) dedicated to St. Vitales and built near the fort in April 1565 by Miguel Lopez de Legazpi, Fray Andrés de Urdaneta and Fray Diego de Herrera.[2]

Construction of the cathedral took many years due to frequent interruptions, brought about by lack of funds and other unexpected events.[3] At one time, funds meant for the building of the cathedral were diverted to the Moro wars. The death of an incumbent bishop who spearheaded the construction/reconstruction and vacancies in the office were also factors.

The architecture of the church is typical of Spanish colonial churches in the country, namely, squat and with thick walls to withstand typhoons and other natural calamities. The facade features a trefoil-shaped pediment, which is decorated with carved relieves of floral motifs, an IHS inscription and a pair of griffins. The Spanish Royal Coat of Arms is emblazoned in low relief above the main entrance, reflecting perhaps the contribution of the Spanish monarch to its construction.

During World War II, much of the cathedral was destroyed by Allied bombings of the city. Only the belfry (built in 1835), the façade, and the walls remained. It was quickly rebuilt in the 1950s under the supervision of architect Jose Ma. Zaragosa,[4] during the incumbency of Archbishop Gabriel Reyes.

In 1982, a mausoleum was built at the back of the sacristy at the initiation of Archbishop Julio Cardinal Rosales.[5] It serves as a final resting place for the remains of Cebu's bishops and clergy. Cardinal Rosales, who died three months after inauguration of the mausoleum, is interred there along with Archbishop Manuel Salvador, a coadjutor archbishop of Cebu, and Archbishop Mariano Gaviola, the archbishop of Lipa (1981–1993). The remains of Bishop Juan Bautista Gorordo, the first Filipino and Cebuano bishop of Cebu, are also interred there.

The cathedral was renovated for the 75th anniversary celebration on April 28, 2009 of the elevation of Cebu into an archdiocese.[6] An application is pending at the Vatican for the cathedral's elevation into a minor basilica[7] in honor of St. Vitalis, an early Christian martyr. His feast day coincides with the day the image of the Sto. Niño de Cebu was found almost 450 years ago, as well as the anniversary of the elevation of Cebu into an archdiocese.

Since early of 2014, the present rector and parish priest of the cathedral is Reverend Monsignor Ruben Labajo
